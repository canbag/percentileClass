function tableToArrayForPercentile($raw){
	$tmp = str_replace(',','.',$raw);
	$tmp = preg_replace('#\t#si',',',$tmp);
	$tmp = preg_replace('#\r\n#si',"),\r\narray(",$tmp);
	if(substr($tmp,(strlen("array(")*-1),strlen("array(")) == 'array('){
		$tmp = substr($tmp,0,(strlen("array(")*-1));
	}
	if(substr($tmp,0,strlen("),")) == '),'){
		$tmp = substr($tmp,strlen("),"));
	}
	
	$tmp = '$data[] = array('.$tmp.');';
	
	
	return $tmp;
